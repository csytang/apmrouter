package org.helios.collector.jdbc.binding.provider;

/**
 * <p>Title: IBindVariableProviderListener</p>
 * <p>Description: Defines a class that will listen on <code>IBindVariableProvider</code> changes.</p> 
 * <p>Company: Helios Development Group</p>
 * @author Whitehead (whitehead.nicholas@gmail.com)
 * @version $LastChangedRevision$
 * $HeadURL$
 * $Id$
 */
public interface IBindVariableProviderListener {
	/**
	 * Fired when an IBindVariableProviders value changes.
	 * @param provider The instance of the IBindVariableProvider that had a value change.
	 * @param oldValue The prior value
	 * @param newValue The new value
	 */
	public void onValueChanged(IBindVariableProvider provider, Object oldValue, Object newValue);
}
